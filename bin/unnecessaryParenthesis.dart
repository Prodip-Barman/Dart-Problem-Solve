void main() {
  double x = 5;
  double y = 4;
  double z = 6;
  double p = 3;
  double q = 4;
  double m = 10;
  double n = 8;

  double value = x - (y / z) + 2 % 8 + 25; //	(x-(y/z))+2%8+25
  print(value);
  print("");

  double val = (x - y) * p + q; //	((x-y)*p)+q
  print(val);
  print("");

  double res = m * n + (-x / y); //	(m*n)+(-x/y)
  print(res);
  print("");

  print(x / 3 * y); // x/(3*Y)I
}
